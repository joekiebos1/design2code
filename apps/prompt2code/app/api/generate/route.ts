/**
 * POST /api/generate
 *
 * Single-call replacement for the old SI → structure two-step chain.
 * Combines creative narrative direction + block architecture into one cached
 * system prompt. Streams sections as SSE events so the preview builds live.
 *
 * Events:
 *   data: {"type":"section","section":{...}}   — one per completed section
 *   data: {"type":"complete","brief":{...}}    — full PageBrief at the end
 *   data: {"type":"error","error":"..."}       — on failure
 */
