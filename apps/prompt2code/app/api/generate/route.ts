// ─── System prompt ────────────────────────────────────────────────────────────

function buildSystemPrompt(): string {
  return `You are a page architect. Fill a product page template with content.

Components:
hero — headline, cta {label, destination}, imageBrief, imageIntent (lifestyle|product|abstract)
proofPoints — headline, items[]: {title, description, icon (IcCheckboxOn|IcStar|IcSecured|IcGlobe|IcHome|IcShoppingBag|IcWifiNetwork|IcHealthy)}
mediaText5050 — headline, body, cta {label, destination}, mediaType (image|none), imageBrief, imageIntent; blockOptions: variant (accordion|paragraphs), paragraphColumnLayout (single|multi)
  accordion: items[]: {subtitle, body}
  single: body field only (no items)
  multi: items[]: {subtitle, body}
carousel — headline, items[]: {title, description}, imageBrief, imageIntent
cardGrid — headline, items[]: {title, description}, imageBrief per item, imageIntent
mediaTextAsymmetric — items[]: {title (question), body (answer)}; blockOptions: variant (faq)

Resolve is appended automatically — do not generate it.

Output valid JSON only:
meta: pageName, slug, audience, primaryAction, keyMessage
sections[]: order, sectionName, component, narrativeRole (setup|engage), imageBrief, imageIntent
  contentSlots: headline, body, cta {label, destination}, mediaType (image|none), items[]
  blockOptions: emphasis, variant, paragraphColumnLayout, imagePosition, imageAspectRatio, cardSize, columns, alignment
status: "draft"
`
}

const SYSTEM_BLOCKS: Anthropic.Messages.TextBlockParam[] = [
  { type: 'text', text: buildSystemPrompt() } as Anthropic.Messages.TextBlockParam & { cache_control: { type: 'ephemeral' } },
]
;(SYSTEM_BLOCKS[0] as Anthropic.Messages.TextBlockParam & { cache_control?: { type: 'ephemeral' } }).cache_control = { type: 'ephemeral' }

// ─── Incremental section extraction ──────────────────────────────────────────

/**
 * Scans the accumulated JSON buffer for complete section objects.
 * Returns any newly completed sections not yet in `seen`.
 */
function extractNewSections(buffer: string, seen: Set<number>): Section[] {
  const match = /"sections"\s*:\s*\[/.exec(buffer)
  if (!match) return []

  const arrayStart = buffer.indexOf('[', match.index)
  if (arrayStart === -1) return []

  const results: Section[] = []
  let depth = 0
  let objStart = -1

  for (let i = arrayStart + 1; i < buffer.length; i++) {
    const c = buffer[i]
    if (c === '{') {
      if (depth === 0) objStart = i
      depth++
    } else if (c === '}') {
      depth--
      if (depth === 0 && objStart !== -1) {
        try {
          const obj = JSON.parse(buffer.slice(objStart, i + 1)) as Section
          if (typeof obj.order === 'number' && !seen.has(obj.order)) {
            seen.add(obj.order)
            results.push(obj)
          }
        } catch {
          // object not yet valid JSON — skip until buffer grows
        }
        objStart = -1
      }
    }
  }

  return results
}

function extractJson(raw: string): string {
  let t = raw.trim().replace(/^\uFEFF/, '')
  const codeBlock = t.match(/```(?:json)?\s*([\s\S]*?)```/)
  if (codeBlock) t = codeBlock[1].trim()
  const first = t.indexOf('{')
  const last = t.lastIndexOf('}')
  if (first !== -1 && last > first) t = t.slice(first, last + 1)
  return t
    .replace(/[\u201C\u201D]/g, '"')
    .replace(/[\u2018\u2019]/g, "'")
    .replace(/[\u0000-\u0008\u000B\u000C\u000E-\u001F]/g, '')
}

function tryParse(t: string): PageBrief | null {
  try { return JSON.parse(t) as PageBrief } catch { /* fall through */ }
  try { return JSON5.parse(t) as PageBrief } catch { /* fall through */ }
  return null
}

