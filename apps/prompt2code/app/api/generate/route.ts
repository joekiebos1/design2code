// ─── Route handler ────────────────────────────────────────────────────────────

export async function POST(req: NextRequest) {
  const apiKey = process.env.ANTHROPIC_API_KEY
  if (!apiKey) {
    return new Response(
      `data: ${JSON.stringify({ type: 'error', error: 'ANTHROPIC_API_KEY not set' })}\n\n`,
      { status: 500, headers: { 'Content-Type': 'text/event-stream' } }
    )
  }

  const body = await req.json()
  const { productName, whatItDoes = '', whatIsInIt = '', builtFor = '', productType = 'hardware', facts, keyMessage, primaryAction } = body

  if (!productName) {
    return new Response(
      `data: ${JSON.stringify({ type: 'error', error: 'productName required' })}\n\n`,
      { status: 400, headers: { 'Content-Type': 'text/event-stream' } }
    )
  }

  const templateId = productType === 'software' ? 'product-page-software' : 'product-page-hardware'
  const template = getTemplate(templateId)!
  const templateInstructions = templateToPrompt(template)

  // New flow: facts[] array from suggestion loop
  // Legacy flow: whatItDoes / whatIsInIt / builtFor freetext
  const hasFacts = Array.isArray(facts) && facts.length > 0

  const userMessage = hasFacts
    ? `Product: ${productName} (${productType})
${keyMessage ? `Key message: ${keyMessage}` : ''}
${primaryAction ? `Primary action: ${primaryAction}` : ''}

Facts provided by the team:
${facts.map((f: string) => `- ${f}`).join('\n')}

${templateInstructions}

Generate the PageBrief as JSON. Output ONLY the JSON object.`
    : `Product: ${productName}

What it does:
${whatItDoes || '(not provided)'}

What is in it:
${whatIsInIt || '(not provided)'}

Built for:
${builtFor || '(not provided)'}

${templateInstructions}

Generate the PageBrief as JSON. Output ONLY the JSON object.`

  const anthropic = new Anthropic({ apiKey })
  const encoder = new TextEncoder()

  const stream = new ReadableStream({
    async start(controller) {
      const push = (event: Record<string, unknown>) => {
        controller.enqueue(encoder.encode(`data: ${JSON.stringify(event)}\n\n`))
      }

      try {
        let buffer = ''
        const seen = new Set<number>()

        const response = anthropic.messages.stream({
          model: 'claude-haiku-4-5-20251001',
          max_tokens: 5000,
          system: SYSTEM_BLOCKS as Anthropic.Messages.TextBlockParam[],
          messages: [{ role: 'user', content: userMessage }],
        })

        for await (const event of response) {
          if (
            event.type === 'content_block_delta' &&
            event.delta.type === 'text_delta'
          ) {
            buffer += event.delta.text
            const newSections = extractNewSections(buffer, seen)
            for (const section of newSections) {
              push({ type: 'section', section })
            }
          }
        }

        // Parse and emit the complete brief for meta, ia, etc.
        const finalMsg = await response.finalMessage()
        const rawText = finalMsg.content.find(c => c.type === 'text')
        const text = rawText?.type === 'text' ? rawText.text : ''
        const brief = tryParse(extractJson(text))

        if (!brief) {
          console.error('[generate] Failed to parse final brief. First 500 chars:', text.slice(0, 500))
          push({ type: 'error', error: 'Failed to parse page brief' })
        } else {
          brief.meta = brief.meta ?? ({} as PageBrief['meta'])
          brief.ia = brief.ia ?? ({} as PageBrief['ia'])
          brief.sections = Array.isArray(brief.sections) ? brief.sections : []
          brief.meta.template = 'product-page'
          brief.meta.pageName = brief.meta.pageName || productName
          brief.createdAt = new Date().toISOString()
          brief.version = 1
          brief.status = 'draft'

          // Enforce template palette overrides on engage sections
          const briefWithOverrides = applyTemplateOverrides(brief, template)
          Object.assign(brief, briefWithOverrides)

          // Append fixed Resolve template — never generated by Claude
          const nextOrder = brief.sections.reduce((max, s) => Math.max(max, s.order ?? 0), 0) + 1
          const resolveSections = buildResolveSections(brief.meta, nextOrder, template)
          brief.sections = [...brief.sections, ...resolveSections]

          push({ type: 'complete', brief })
        }
      } catch (err) {
        console.error('[generate] Stream error:', err)
        push({ type: 'error', error: err instanceof Error ? err.message : 'Unknown error' })
      } finally {
        controller.close()
      }
    },
  })

  return new Response(stream, {
    headers: {
      'Content-Type': 'text/event-stream',
      'Cache-Control': 'no-cache, no-transform',
      Connection: 'keep-alive',
    },
  })
}
